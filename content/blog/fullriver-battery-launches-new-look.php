<strong>Camarillo, CA, May 14, 2015</strong> - Fullriver Battery USA, manufacturer of high-quality, maintenance-free, deep-cycle AGM batteries, announces a rebranding of the company’s identity.  The rebranding campaign, centers around a fresh new logo, and is focused on brand continuity throughout their product series.
